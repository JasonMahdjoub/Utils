package com.distrimind.util.crypto;

import java.security.SecureRandom;

/**
 * 
 * @author Jason Mahdjoub
 * @version 1.0
 * @since Utils 2.0
 */
public abstract class AbstractSecureRandom extends SecureRandom
{
    /**
     * 
     */
    private static final long serialVersionUID = 8080761472279881205L;

    private final SecureRandomType type;

    AbstractSecureRandom(SecureRandomType type)
    {
	super(null, null);
	this.type = type;
    }

    /**
     * Returns the specified number of seed bytes.
     * 
     * @param numBytes
     *            number of seed bytes to get
     * 
     * @return an array containing the seed bytes
     */
    @Override
    public abstract byte[] generateSeed(int numBytes);

    /**
     * Returns the algorithm name used or "unknown" when the algorithm used
     * couldn't be determined (as when constructed by the protected 2 argument
     * constructor).
     *
     */
    @Override
    public abstract String getAlgorithm();

    public abstract gnu.vm.jgnu.security.SecureRandom getGnuSecureRandom();

    public abstract java.security.SecureRandom getJavaNativeSecureRandom();

    public final SecureRandomType getType()
    {
	return type;
    }

    /**
     * Generates a user specified number of bytes. This function is the basis
     * for all the random functions.
     * 
     * @param bytes
     *            array to store generated bytes in
     */
    @Override
    public abstract void nextBytes(byte[] bytes);

    /**
     * Seeds the SecureRandom. The class is re-seeded for each call and each
     * seed builds on the previous seed so as not to weaken security.
     * 
     * @param seed
     *            seed bytes to seed with
     */
    @Override
    public abstract void setSeed(byte[] seed);

    @Override
    public abstract void setSeed(long seed);

}
