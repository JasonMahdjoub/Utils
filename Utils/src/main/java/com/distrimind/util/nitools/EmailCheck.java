package com.distrimind.util.nitools;

import java.util.regex.Pattern;

/**
 * @author Jason Mahdjoub
 * @version 1.0
 * @since Utils 3.21
 */
public class EmailCheck {

    private static final String regexEmailValidation = "(?:[a-zA-Z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-zA-Z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-zA-Z0-9](?:[a-zA-Z0-9-]*[a-zA-Z0-9])?\\.)+[a-zA-Z0-9](?:[a-zA-Z0-9-]*[a-zA-Z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-zA-Z0-9-]*[a-zA-Z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)])";
    private static final Pattern patternEmailValidation=Pattern.compile(regexEmailValidation);

    public static String getDefaultRegexEmailValidation() {
        return regexEmailValidation;
    }

    public static Pattern getDefaultPatternEmailValidation() {
        return patternEmailValidation;
    }

    public static boolean isValidMail(String email)
    {
        return isValidMail(email, true);
    }
    public static boolean isValidMail(String email, boolean checkMail)
    {
        return isValidMail(patternEmailValidation, email, checkMail);
    }
    public static boolean isValidMail(Pattern pattern, String email, boolean checkMail)
    {
        assert pattern!=null;
        return pattern.matcher(email).matches() && (!checkMail || DNSCheck.isUsedDomainFromEmail(email));
    }
}
